    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        Cookie[] cookie = request.getCookies();
        if (cookie != null) {
            for (Cookie c : cookie) {
                if (c.getName().equals("c_user")) {
                    request.setAttribute("user_name", c.getValue());
                }
                if (c.getName().equals("c_pass")) {
                    request.setAttribute("pass_word", c.getValue());
                }
            }
        }
        request.getRequestDispatcher("login.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            UserDAO us = new UserDAO();
            String mail = request.getParameter("email");
            String pass = request.getParameter("password");
            User user = us.checkLogin(mail, pass);
            
            if (user != null) {
                HttpSession session = request.getSession();
                session.setAttribute("acc", user);
                session.setAttribute("size", 0);
                response.sendRedirect("homeservlet");
                
            } else {
                request.setAttribute("error", "Thông tin đăng nhập không chính xác.");
                request.getRequestDispatcher("login.jsp").forward(request, response);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

